export function useAppTranslation() {
	const { t, i18n } = useTranslation();

	const translate = (
		key: string,
		options?: string | { defaultValue?: string;[key: string]: unknown },
	) => {
		const resolvedOptions =
			typeof options === "string" ? { defaultValue: options } : (options ?? {});
		const fallback = resolvedOptions.defaultValue ?? EN_MESSAGES[key] ?? key;
		return t(key, { ...resolvedOptions, defaultValue: fallback });
	};

	return { t: translate, i18n };
}
